print("What Are Operators?")
print("Operators are symbols used to perform operations on variables and values.")
# =========================================
# OPERATORS IN PYTHON
# =========================================

print("OPERATORS IN PYTHON\n")

# -----------------------------------------
# 1. ARITHMETIC OPERATORS
# -----------------------------------------

print("1. ARITHMETIC OPERATORS")

a = 10
b = 3

print("a + b =", a + b)   # Addition
print("a - b =", a - b)   # Subtraction
print("a * b =", a * b)   # Multiplication
print("a / b =", a / b)   # Division
print("a % b =", a % b)   # Modulus (Remainder)
print("a ** b =", a ** b) # Power
print("a // b =", a // b) # Floor Division

print("\n")

# -----------------------------------------
# 2. COMPARISON OPERATORS
# -----------------------------------------

print("2. COMPARISON OPERATORS")

print("a == b :", a == b)
print("a != b :", a != b)
print("a > b  :", a > b)
print("a < b  :", a < b)
print("a >= b :", a >= b)
print("a <= b :", a <= b)

print("\n")

# -----------------------------------------
# 3. LOGICAL OPERATORS
# -----------------------------------------

print("3. LOGICAL OPERATORS")

x = True
y = False

print("x and y :", x and y)
print("x or y  :", x or y)
print("not x   :", not x)

print("\n")

# -----------------------------------------
# 4. ASSIGNMENT OPERATORS
# -----------------------------------------

print("4. ASSIGNMENT OPERATORS")

c = 5
print("Initial c:", c)

c += 2
print("c += 2 :", c)

c -= 1
print("c -= 1 :", c)

print("\n")

# -----------------------------------------
# 5. IDENTITY OPERATORS
# -----------------------------------------

print("5. IDENTITY OPERATORS")

m = 10
n = 10

print("m is n :", m is n)
print("m is not n :", m is not n)

print("\n")

# -----------------------------------------
# 6. MEMBERSHIP OPERATORS
# -----------------------------------------

print("6. MEMBERSHIP OPERATORS")

text = "Python"

print("'P' in text :", 'P' in text)
print("'z' not in text :", 'z' not in text)

print("\nProgram Completed Successfully!")